def count_letters(string): #Define a function called "count_letters" that takes a string as input.
  """Python function that takes a string and counts the number of uppercase and lowercase letters."""
  uppercase_count = 0 #Initialize variable "uppercase_count" to 0 to keep track of the counts of uppercase letters.
  lowercase_count = 0 #Initialize variable "lowercase_count" to 0 to keep track of the counts of lowercase letters.

  for character in string: #Use a loop to Return both "uppercase_count" and "lowercase_count" as the result.iterate through each character in the input string.
    if character.isupper(): #For each character, check if it's an uppercase letter using the "isupper()" method.
       uppercase_count += 1 #If it is, increment the "uppercase_count" by 1.
    elif character.islower(): #Check if it's a lowercase letter using the "islower()" method.
       lowercase_count += 1 #If it is, increment the "lowercase_count" by 1.

  return uppercase_count, lowercase_count #Return both "uppercase_count" and "lowercase_count" as the result.


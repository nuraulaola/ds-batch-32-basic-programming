def is_prime(number): #Define a function called "is_prime" that takes a number as input.
  """Python function that takes a number as an input parameter and checks whether it is prime or not."""
  if number <= 1:
      return False  # Numbers less than or equal to 1 are not prime.
  elif number <= 3:
      return True  # 2 and 3 are prime numbers.
  elif number % 2 == 0 or number % 3 == 0:
      return False  # Numbers divisible by 2 or 3 are not prime.
  i = 5
  while i * i <= number:
      if number % i == 0 or number % (i + 2) == 0:
          return False  # Numbers divisible by factors other than 1, itself, 2, and 3 are not prime
      i += 6
  return True  # If all tests fail, the number is prime

#Example
number = 17
if is_prime(number):
    print(number, "is a prime number.")
else:
    print(number, "is not a prime number.")

